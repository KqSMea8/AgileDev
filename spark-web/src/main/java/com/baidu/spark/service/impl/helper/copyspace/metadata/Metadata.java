package com.baidu.spark.service.impl.helper.copyspace.metadata;

import java.util.List;

import com.baidu.spark.service.impl.helper.copyspace.option.ImportOption;

/**
 * 导出数据包装器
 * @author zhangjing_pe
 *
 */
public class Metadata {
	/**元数据的json字符串	 */
	protected String resultData;
	/**导入的确认选项*/
	protected List<ImportOption<?>> importOptions;
	/**导出的确认选项*/
	protected List<?> exportConfirmInfo;
	
	public String getResultData() {
		return resultData;
	}

	public void setResultData(String resultData) {
		this.resultData = resultData;
	}

	public List<ImportOption<?>> getImportOptions() {
		return importOptions;
	}

	public void setImportOptions(List<ImportOption<?>> importOptions) {
		this.importOptions = importOptions;
	}

	public List<?> getExportConfirmInfo() {
		return exportConfirmInfo;
	}

	public void setExportConfirmInfo(List<?> exportConfirmInfo) {
		this.exportConfirmInfo = exportConfirmInfo;
	} 
	
	
}
